# Ints
print("Type of 4:", type(4))
print("Type of -256:", type(-256))
print("Type of 1000000:", type(1000000), "\n")

# Floats
print("Type of 3.14:", type(3.14))
print("Type of -9.81:", type(-9.81), "\n")

# Division type
print("Dividing two (3 / 4) integers gets:", type(3/4), "\n")

# Strings
print("Type of 'Hello':", type("Hello"))
print("You can mix double-quotes and single-quotes, but be careful!")
print("don't")
